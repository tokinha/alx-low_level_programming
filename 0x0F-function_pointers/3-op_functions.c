#include "3-calc.h"

/**
 * op_add - add two integer
 * @a: first int
 * @b: secnd int
 *
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - suptract two integers
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integers
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - module two integers
 * @a: 1st int
 * @b: 2nd int
 *
 * Return: reminder
*/
int op_mod(int a, int b)
{
	return (a % b);
}
